// Filter helper: matches city or country, city-name matches first.
export function suggestCities(query, limit = 5) {
  const q = query.trim().toLowerCase();
  if (q.length < 2) return [];
  const starts = [];
  const contains = [];
  for (const c of CITY_LIST) {
    const name = c[0].toLowerCase();
    const country = c[1].toLowerCase();
    if (name.startsWith(q)) { starts.push(c); if (starts.length >= limit) break; }
    else if (contains.length < limit && (name.includes(q) || country.startsWith(q))) contains.push(c);
  }
  return [...starts, ...contains].slice(0, limit);
}
